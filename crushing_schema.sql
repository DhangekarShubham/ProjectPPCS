CREATE TABLE DAILY_CRUSHING (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CRUSH_DATE DATE NOT NULL,
    CROP_DAY NUMBER NOT NULL,
    CANE_ON_DATE NUMBER(15,3),
    CANE_TO_DATE NUMBER(15,3),
    SUGAR_ON_DATE NUMBER(15,3),
    SUGAR_TO_DATE NUMBER(15,3),
    PERCENT_ON_DATE NUMBER(5,2),
    PERCENT_TO_DATE NUMBER(5,2),
    MILL_EXT_ON_DATE NUMBER(5,2),
    MILL_EXT_TO_DATE NUMBER(5,2),
    REDUCED_EXT_ON_DATE NUMBER(5,2),
    REDUCED_EXT_TO_DATE NUMBER(5,2),
    COGEN_ON_DATE NUMBER(10,2),
    COGEN_TO_DATE NUMBER(10,2),
    CONSTRAINT UK_CRUSH UNIQUE (CRUSH_DATE, CROP_DAY)
);

CREATE TABLE CRUSHING_FIELD_CONFIG (
    FIELD_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
